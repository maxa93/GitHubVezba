public class CountElementInArray {
    public static void main(String[]args){
        String [] mojaLista = {"jedan","dva","tri"};
        int count = mojaLista.length;;
        System.out.println("The number of elements are: " + count);
        for (int i=0; i<mojaLista.length; i++){
            System.out.println(mojaLista[i]+"");
        }
    }
}
